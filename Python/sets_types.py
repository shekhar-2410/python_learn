# sets

collection = {1,2,3,4,4,5,"hello","hello"}

print(collection)
print(type(collection))

# set methods

# add method --- add an element
collection.add(6)
print(collection)

# remove method --- remove an element
collection.remove(6)
print(collection)

# clear method --- remove all elements
collection.clear()
# print(collection)

# pop  --- remove last element
collection.pop()
print(collection)

