# Numeric types
x = 10            # int
y = 3.14          # float
z = 2 + 3j        # complex

# Sequence types
name = "Alice"    # str
fruits = ["apple", "banana", "cherry"]  # list
coordinates = (4, 5)   # tuple
numbers = range(5)     # range

# Mapping type
person = {"name": "John", "age": 30}  # dict

# Set types
unique_numbers = {1, 2, 3, 4}   # set
frozen_set = frozenset([1, 2, 3])  # frozenset

# Boolean type
is_sunny = True   # bool

# Binary types
binary_data = b"hello"   # bytes
mutable_bytes = bytearray(5)  # bytearray

# None type
result = None   # None